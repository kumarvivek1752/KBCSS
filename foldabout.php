</br></br>The basic idea of KBCSS is to provide an environment for the students and faculty of a university to enhance their technical skills and to share their knowledge with their fellow students or faculty/researchers/industrial experts. It also aims to provide a platform for mutual cooperation between different kinds of users, learning by mining(i.e., previous discussions are a source which is studied). The system was proposed to replace the existing e-learning systems of many universities and colleges which are unidirectional i.e., from faculty to students to a bidirectional way i.e., also from student to faculty.
